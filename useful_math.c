#include <stdio.h>
#include <math.h>
int main(){
   int ceil_val = ceil(2.5);
   printf("The ceil value is: %d\n", ceil_val);
   int floor_val = floor(2.5);
   printf("The floor value is: %d\n", floor_val);
  int round_val = round(2.5);
   printf("The round value is: %d\n", round_val);
   return 0;
};